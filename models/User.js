const mongoose = require('mongoose');

const UserSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
}, {
    timestamps: true,  // to add createdAt and updatedAt fields
});

module.exports = mongoose.model('User', UserSchema);
